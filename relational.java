public class relationalOperater {
    public static void main(String[] args) {
        int p = 2, q = 4;

        System.out.println(p == q);  //f
        System.out.println(p != q); //t
        System.out.println(p <= q); //t
        System.out.println(p >= q); //f
        System.out.println(p < q); //t
        System.out.println(p > q); //f

    }

}
