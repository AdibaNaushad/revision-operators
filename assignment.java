import java.util.Scanner;

public class assignment {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int p = sc.nextInt();
        int q = sc.nextInt();

        System.out.println(p += q);
        System.out.println(p -= q);
        System.out.println(p *= q);
        System.out.println(p /= q);

    }

}
